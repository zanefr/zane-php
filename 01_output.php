<?php 

// Echo - Output strings, numbers, html, etc

echo 'Hello';

// print - Similar to echo, but a bit slower. can only take in a single argument
print 'Hello';

// print_r - Gives a bit more info. Can be used to print arrays and single values

print_r([1, 2, 3]);

// var_dump - Even more info like data type and length
var_dump('Hello');

var_dump([1, 2, 3]);

//var_export() - similar to var_dump(). outputs a string representation of a variable
 var_export('hello');

// Escaping characters with a backslash
echo "Is your name O\'reilly?";


?>